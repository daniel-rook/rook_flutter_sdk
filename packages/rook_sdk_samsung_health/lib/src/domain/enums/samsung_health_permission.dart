/// Represents the different types of permissions available in Samsung Health.
/// Each enum constant corresponds to a specific data type that can be accessed
/// from Samsung Health.
enum SamsungHealthPermission {
  activitySummary,
  bloodGlucose,
  bloodOxygen,
  bloodPressure,
  bodyComposition,
  exercise,
  exerciseLocation,
  floorsClimbed,
  heartRate,
  nutrition,
  sleep,
  steps,
  waterIntake,
}
